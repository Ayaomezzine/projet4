ceci est un script qui permet d'analyser /etc/passwd et afficher son contenu 
sous forme plus lisible;
Utilisez les options suivantes:
-g : afficher un menu textuel et gerer les fonctionalités de façon graphiques
-v : afficher le nom des auteurs et version de code 
-m : afficher un menu textuel 
-n : afficher le nombre d'utlisateurs
-p : afficher les repertoires personnels des utilisateurs
-G : utilisateurs appartenant au meme groupe
-t : type d'utilisateurs 


